age = 12;
switch(age){
    case 5:
        console.log(`Age is 5`);
        break;
    case 10:
        console.log(`Age is 10`);
        break;
    case 15:
        console.log(`Age is 15`);
        break;
    default:
        console.log(`Age is ${age}`)
}
